package week10.d05.senior;

public class MinMaxSum {

    private final int minSum;
    private final int maxSum;

    public MinMaxSum(int minSum, int maxSum) {
        this.minSum = minSum;
        this.maxSum = maxSum;
    }

    public int getMinSum() {
        return minSum;
    }

    public int getMaxSum() {
        return maxSum;
    }
}
